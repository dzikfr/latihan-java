    private void tabelMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tabelMenuActionPerformed
        DefaultTableModel tableModel = (DefaultTableModel) tabelData.getModel();
        
            Object nimTabel = nim.getText(),   
                   namaTabel = nama.getText(),
                   genderTabel = lakiButton.isSelected()? "Laki-Laki" : "Perempuan",
                   jurusanTabel = jurusan.getSelectedItem(),
                   semesterTabel = semester.getText(),
                   gradeTabel = grade.getText();
           

            tableModel.addRow(new Object[]{nimTabel, namaTabel, genderTabel,
                                           jurusanTabel, semesterTabel, gradeTabel
                                           });

    }//GEN-LAST:event_tabelMenuActionPerformed

