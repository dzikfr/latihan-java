    private void countActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_countActionPerformed
        double hadir = Double.parseDouble(nilaiKehadiran.getText()),
                tugas = Double.parseDouble(nilaiTugas.getText()),
                hasilUts = Double.parseDouble(nilaiUts.getText()),
                hasilUas = Double.parseDouble(nilaiUas.getText()),
                regHitung = Double.parseDouble(regValue.getText()),
                utsHitung = Double.parseDouble(utsValue.getText()),
                uasHitung = Double.parseDouble(uasValue.getText()),
                hasilPemrograman, total;
        
        hasilPemrograman = (Double)((hadir *10/100)+(tugas*20/100)+(hasilUts*30/100)+(hasilUas*40/100));
        nilaiPemrograman.setText(""+hasilPemrograman);
        
        if(hasilPemrograman <= 100 && hasilPemrograman >=80){
            grade.setText("A");
        }else if(hasilPemrograman <80 && hasilPemrograman >=70){
            grade.setText("B");
        }else if(hasilPemrograman <70 && hasilPemrograman >=60){
            grade.setText("C");
        }else if(hasilPemrograman <60 && hasilPemrograman >=50){
            grade.setText("D");
        }else if(hasilPemrograman <50 && hasilPemrograman >=0){
            grade.setText("E");
        }else{
            grade.setText("nilai tidak valid");
        }
        
        String gender;
        if(lakiButton.isSelected()){
            perempuanButton.setSelected(false);
            gender = "Laki-Laki";
        }else {
            perempuanButton.setSelected(true);
            lakiButton.setSelected(false);
            gender = "Perempuan";
        }
        
        String jurusanDipilih;
        if(jurusan.getSelectedItem()=="Teknik Informatika"){
            jurusanDipilih = "Teknik Informatika";
        }else if(jurusan.getSelectedItem()=="Ekonomi"){
            jurusanDipilih = "Ekonomi";
        }else if(jurusan.getSelectedItem()=="MIPA"){
            jurusanDipilih = "MIPA";
        }else{
            jurusanDipilih = "Anda Belum Memilih Jurusan";
        }
        
        total = regHitung + utsHitung + uasHitung;
        
        valueData.setText("NIM \t : " + nim.getText()
                         +"\nNAMA \t : " + nama.getText()
                         +"\nJenis Kelamin \t : " + gender.toString()
                         +"\nJurusan \t : " + jurusanDipilih.toString()
                         +"\nSemester \t : " + semester.getText()
                         +"\nNilai Pemp.2 \t : " + nilaiPemrograman.getText()
                         +"\nGrade \t : " + grade.getText()
                         +"\nTotal \t : " + total);
    }//GEN-LAST:event_countActionPerformed

